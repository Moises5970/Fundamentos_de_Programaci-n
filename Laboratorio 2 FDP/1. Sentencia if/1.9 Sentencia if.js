// Verifica si un número es igual a 0.
// Variable del número que el usuario ingresa.
//parseInt convierte la cadena del prompt en números.
let num = parseInt(prompt("Ingresa un número"));

// Condicional que decidirá si el número ingresado es 0.
if(num === 0){
    console.log("Es igual a 0");
} 

