// Determina si un número es positivo o negativo.
//Variable del número que el usuario ingresa.
let num = prompt("Ingresa un número");

//Condicional para saber si el número es positivo o negativo.
//Si el número es positivo, entonces entra en la primera opción.
//Si no, entonces entra en la segunda opción.
if (num > 0) {
    console.log("El número es positivo");
} else {
    console.log("El número es negativo");
}

//Fin.