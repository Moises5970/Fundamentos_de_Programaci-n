//Verifica si una persona es adolescente (entre 13 y 19 años).
//Variable para la edad de la persona.
let edad = prompt("¿Cuál es su edad?");

//Condicional para saber si la perosona es adolescente o no.
//Si la edad es mayor o igual a 13 años, entonces entra en la primer opción.
//Si no, entonces entra en la segunda opción.
if (edad >= 13 && edad <= 19) {
    console.log("Es adolescente");
} else {
    console.log("No es adolescente");
}   

