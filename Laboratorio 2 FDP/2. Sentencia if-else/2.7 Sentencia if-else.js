//Verifica si un número es divisible entre 2 o entre 3.
//Variable del número que el usuario va a ingresar.
let num = prompt("Ingresa un número");

//Condicional para saber si el número es divisible por 2 o por 3.
//Si es true, entonces entra en la primera opción.
if (num % 2 === 0) {
    console.log("El número es divisible entre 2");
//Si no, entonces entra en la segunda opción.
} else (num % 3 === 0); {
    console.log("El número es divisible entre 3");
} 

//Fin.